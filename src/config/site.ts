export const getArticleBySlug = (slug: string) => {
    for (const section of siteConfig.sections) {
      if (!section.items) continue;
      const article = section.items.find((item: any) => item.slug === slug);
      if (article) {
        return article;
      }
    }
    return null;
  };
  
